CREATE TABLE T_RECONCILIATION_MATCH (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  RECONCILIATION_ID BIGINT NOT NULL,
  INTERDATA_SALE_ID BIGINT,
  ACQ_PROVIDER VARCHAR(10) NOT NULL,
  ACQ_SALE_ID BIGINT,
  MATCH_RULE VARCHAR(50),
  MATCH_META BLOB SUB_TYPE TEXT,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IX_RECON_MATCH_RECON_ID ON T_RECONCILIATION_MATCH (RECONCILIATION_ID);
CREATE INDEX IX_RECON_MATCH_INTERDATA ON T_RECONCILIATION_MATCH (INTERDATA_SALE_ID);
CREATE INDEX IX_RECON_MATCH_ACQ ON T_RECONCILIATION_MATCH (ACQ_PROVIDER, ACQ_SALE_ID);
CREATE UNIQUE INDEX UX_RECON_MATCH_RECON_ID ON T_RECONCILIATION_MATCH (RECONCILIATION_ID);
