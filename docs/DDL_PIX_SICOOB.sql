CREATE TABLE PIX_RAW (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PROVIDER VARCHAR(20) NOT NULL,          -- 'SICOOB'
  REF_DATE DATE NOT NULL,
  SOURCE_TYPE VARCHAR(10) NOT NULL,       -- 'API'
  PAYLOAD BLOB SUB_TYPE TEXT,
  PAYLOAD_SHA256 CHAR(64),
  IMPORTED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IDX_PIX_RAW_REF ON PIX_RAW (PROVIDER, REF_DATE);

CREATE TABLE PIX_TX (
  ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  PROVIDER VARCHAR(20) NOT NULL,          -- 'SICOOB'
  REF_DATE DATE NOT NULL,

  END_TO_END_ID VARCHAR(50),
  TXID VARCHAR(40),
  VALOR NUMERIC(15,2),
  CHAVE VARCHAR(140),
  DEVOLUCAO_STATUS VARCHAR(30),
  STATUS VARCHAR(30),
  HORARIO TIMESTAMP,
  PAGADOR_CPF_CNPJ VARCHAR(20),
  PAGADOR_NOME VARCHAR(140),

  RAW_ID BIGINT,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX IDX_PIX_TX_REF ON PIX_TX (PROVIDER, REF_DATE);
CREATE INDEX IDX_PIX_TX_KEYS ON PIX_TX (PROVIDER, TXID, END_TO_END_ID);

COMMIT;
